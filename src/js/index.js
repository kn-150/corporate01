// ハンバーガーメニューボタン
$(document).ready(function () {
  $(".open-btn").click(function () {
    $(this).toggleClass('active');
    $(".header_nav").toggleClass('panel-active');//ナビゲーションにpanel-activeクラスを付与
  });
});

$(document).ready(function () {
  $(".header_nav a").click(function () {//ナビゲーションのリンクがクリックされたら
    $(".open-btn").removeClass('active');//ボタンの activeクラスを除去し
    $("header_nav").removeClass('panel-active');//ナビゲーションのpanel-activeクラスも除去
  });
});

$(function(){
  $('.check').on('click', function() {
    if ($(this).prop('checked')){
      // 一旦全てをクリアして再チェックする
      $('.check').prop('checked', false);
      $(this).prop('checked', true);
    }
  });
});
